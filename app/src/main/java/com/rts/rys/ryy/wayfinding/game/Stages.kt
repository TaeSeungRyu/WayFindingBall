        val counter = mutableMapOf<Int, Int>()
        specs.mapIndexed { i, spec ->
            val idx = (counter[spec.level] ?: 0) + 1
            counter[spec.level] = idx
            Stage(
                id = i + 1,
                level = spec.level,
                indexInLevel = idx,
                name = "난이도${spec.level}-${idx}",
