data class Stage(
    val id: Int,
    val level: Int,
    val indexInLevel: Int,
    val name: String,
    val difficulty: String,
    val maze: Maze,
    val isCustom: Boolean = false
)

fun difficultyLabel(level: Int): String = when (level) {
    1 -> "쉬워요"
    2 -> "보통이에요"
    3 -> "조금 어려워요"
    else -> "엄청 어려워요"
}

